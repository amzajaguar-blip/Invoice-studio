#!/usr/bin/env python3
"""
Patches android/app/build.gradle (generated by expo prebuild) to add
a release signingConfig block and switch the release buildType to use it.

Also enables ProGuard for release builds and writes proguard-rules.pro
with React Native safe rules.

Environment variables consumed:
  ANDROID_KEYSTORE_PATH     – path to the keystore file (relative to android/app/)
  ANDROID_KEYSTORE_PASSWORD – store password
  ANDROID_KEY_ALIAS         – key alias
  ANDROID_KEY_PASSWORD      – key password
"""
import re
import os
import shutil

gradle_path = os.path.join('android', 'app', 'build.gradle')

with open(gradle_path, 'r') as f:
    content = f.read()

release_block = '''
        release {
            def ksPath = System.getenv("ANDROID_KEYSTORE_PATH") ?: "debug.keystore"
            def ksPass = System.getenv("ANDROID_KEYSTORE_PASSWORD") ?: "android"
            def kAlias = System.getenv("ANDROID_KEY_ALIAS") ?: "invoicestudio"
            def kPass  = System.getenv("ANDROID_KEY_PASSWORD") ?: "android"
            storeFile file(ksPath)
            storePassword ksPass
            keyAlias kAlias
            keyPassword kPass
        }'''

# Insert release block after the closing brace of the debug signingConfig.
# Expo SDK 53 (RN 0.79) generates:
#   signingConfigs {
#       debug {
#           storeFile file('debug.keystore')
#           storePassword 'android'
#           keyAlias 'androiddebugkey'
#           keyPassword 'android'
#       }
#   }
# We insert the release block right after the debug block's closing brace.
# Use a simpler approach: replace the entire signingConfigs block.
old_signing_configs = '''    signingConfigs {
        debug {
            storeFile file('debug.keystore')
            storePassword 'android'
            keyAlias 'androiddebugkey'
            keyPassword 'android'
        }
    }'''
new_signing_configs = '''    signingConfigs {
        debug {
            storeFile file('debug.keystore')
            storePassword 'android'
            keyAlias 'androiddebugkey'
            keyPassword 'android'
        }
        release {
            def ksPath = System.getenv("ANDROID_KEYSTORE_PATH") ?: "debug.keystore"
            def ksPass = System.getenv("ANDROID_KEYSTORE_PASSWORD") ?: "android"
            def kAlias = System.getenv("ANDROID_KEY_ALIAS") ?: "invoicestudio"
            def kPass  = System.getenv("ANDROID_KEY_PASSWORD") ?: "android"
            storeFile file(ksPath)
            storePassword ksPass
            keyAlias kAlias
            keyPassword kPass
        }
    }'''
content = content.replace(old_signing_configs, new_signing_configs)

# Switch release buildType to use release signingConfig.
# CRITICAL: only patch inside the `release { }` block, NOT the `debug { }` block.
# Expo 53 generates both:
#   debug   { signingConfig signingConfigs.debug }
#   release { signingConfig signingConfigs.debug }
# A global replace would change debug's signing config to .release, which then
# tries to read key alias 'invoicestudio' from Expo's default debug.keystore
# (which only has 'androiddebugkey'). DOTALL lets the pattern span lines.
content = re.sub(
    r"(release\s*\{[^}]*?signingConfig\s+signingConfigs\.)debug",
    r"\1release",
    content,
    flags=re.DOTALL
)

# ── Force JS bundle to be embedded in debug builds ───────────────────────────
# By default the React Native Gradle plugin treats "debug" as a debuggable
# variant and skips createBundleDebugJsAndAssets, so the APK has no
# index.android.bundle and shows a white screen when Metro is unavailable.
if not re.search(r'(?m)^(?!\s*//).*debuggableVariants\s*=', content):
    content = re.sub(
        r'(autolinkLibrariesWithApp\(\)\n)(\})',
        r'\1    // Standalone debug APK: embed the JS bundle instead of relying on Metro\n    debuggableVariants = []\n\2',
        content
    )
    print('✅ build.gradle patched: debuggableVariants = [] (bundle JS in debug)')
else:
    print('ℹ️  debuggableVariants already set, skipping')

# ── 16 KB page alignment for native libraries ──────────────────────────────
# Play Console requires .so libraries to be aligned to 16 KB page size for
# Android 15+ compatibility. Without this, the AAB is rejected.
if 'useLegacyPackaging' in content:
    content = content.replace(
        "useLegacyPackaging (findProperty('expo.useLegacyPackaging')?.toBoolean() ?: false)",
        "useLegacyPackaging true"
    )
else:
    content = re.sub(
        r'(packagingOptions\s*\{\s*jniLibs\s*\{)',
        r'\1\n            useLegacyPackaging true',
        content
    )
print('✅ build.gradle patched: useLegacyPackaging = true (16 KB alignment)')

with open(gradle_path, 'w') as f:
    f.write(content)

print('✅ build.gradle patched for release signing')

# ── Enable ProGuard/R8 in gradle.properties ──────────────────────────────────
props_path = os.path.join('android', 'gradle.properties')
if os.path.exists(props_path):
    with open(props_path, 'r') as f:
        props = f.read()
    # Set enableProguardInReleaseBuilds to true
    props = re.sub(
        r'android\.enableProguardInReleaseBuilds\s*=\s*.*',
        'android.enableProguardInReleaseBuilds=true',
        props
    )
    # Enable R8 full mode + shrinkResources
    if 'android.enableR8.fullMode' not in props:
        props += '\nandroid.enableR8.fullMode=true\n'
    else:
        props = re.sub(
            r'android\.enableR8\.fullMode\s*=\s*.*',
            'android.enableR8.fullMode=true',
            props
        )
    if 'android.enableShrinkResourcesInReleaseBuilds' not in props:
        props += '\nandroid.enableShrinkResourcesInReleaseBuilds=true\n'
    else:
        props = re.sub(
            r'android\.enableShrinkResourcesInReleaseBuilds\s*=\s*.*',
            'android.enableShrinkResourcesInReleaseBuilds=true',
            props
        )
    # Force useLegacyPackaging=true for 16 KB page alignment
    if 'expo.useLegacyPackaging' not in props:
        props += '\nexpo.useLegacyPackaging=true\n'
    else:
        props = re.sub(
            r'expo\.useLegacyPackaging\s*=\s*.*',
            'expo.useLegacyPackaging=true',
            props
        )
    # If the line doesn't exist, append it
    if 'android.enableProguardInReleaseBuilds' not in props:
        props += '\nandroid.enableProguardInReleaseBuilds=true\n'
    with open(props_path, 'w') as f:
        f.write(props)
    print('✅ gradle.properties updated: ProGuard enabled for release')
else:
    print('⚠️  android/gradle.properties not found, skipping ProGuard enable')

# ── Copy proguard-rules.pro into android/app/ ─────────────────────────────────
# Script runs from mobile/ (CI working dir), so proguard-rules.pro is in cwd.
proguard_src = os.path.join('.', 'proguard-rules.pro')
proguard_dst = os.path.join('android', 'app', 'proguard-rules.pro')
if os.path.exists(proguard_src):
    shutil.copy(proguard_src, proguard_dst)
    print('✅ proguard-rules.pro copied into android/app/')
else:
    print('⚠️  proguard-rules.pro not found in mobile/, skipping')
